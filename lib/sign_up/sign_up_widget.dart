  String? _passwordValidator(String? value) {
    if (value == null || value.isEmpty) {
      return 'Введите пароль';
    }
    if (value.length < 8) {
      return 'Пароль должен содержать не менее 8 символов';
    }

    // Проверка наличия цифр
    final hasDigits = RegExp(r'\d').hasMatch(value);
    if (!hasDigits) {
      return 'Пароль должен содержать цифры';
    }

    // Проверка наличия букв
    final hasLetters = RegExp(r'[a-zA-Z]').hasMatch(value);
    if (!hasLetters) {
      return 'Пароль должен содержать буквы';
    }

    // Проверка наличия специальных символов
    final hasSpecialCharacters = RegExp(r'[!@#$%^&*(),.?":{}|<>]').hasMatch(value);
    if (!hasSpecialCharacters) {
      return 'Пароль должен содержать специальные символы';
    }

    return null;
  }

